export const getProfessionsByLetter = (): Record<string, Profession[]> => {
  const result: Record<string, Profession[]> = {};
  professions.forEach(p => {
    if (!result[p.letter]) result[p.letter] = [];
    result[p.letter].push(p);
  });
  return result;
};

export const getProfessionById = (id: string): Profession | undefined => {
  return professions.find(p => p.id === id);
};

export const getRelatedProfessions = (ids: string[]): Profession[] => {
  return ids.map(id => professions.find(p => p.id === id)).filter(Boolean) as Profession[];
};

